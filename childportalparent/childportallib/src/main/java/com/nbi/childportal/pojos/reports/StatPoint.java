package com.nbi.childportal.pojos.reports;


/**
 * @author zahmad
 *
 */
public class StatPoint {
	
	private int month;
	private int year;
	private Long count;
	
	
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public Long getCount() {
		return count;
	}
	public void setCount(Long count) {
		this.count = count;
	}
	
	
	
	

}
